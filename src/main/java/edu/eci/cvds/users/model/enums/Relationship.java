package edu.eci.cvds.users.model.enums;

/**
* Enumeration representing possible relationships between a student and their emergency contact.
* 
* @author Jesús Pinzón (Team Bismuto)
* @version 1.1
* @since 2025-05-09
*/
public enum Relationship {
   
   AUNT,           // Tía
   BROTHER,        // Hermano
   COUSIN,         // Primo/a
   FATHER,         // Padre
   FRIEND,         // Amigo/a
   GRANDPARENT,    // Abuelo/a
   GUARDIAN,       // Tutor legal
   MOTHER,         // Madre
   OTHER,          // Otro
   PARTNER,        // Pareja
   SIBLING,        // Hermano/a (genérico)
   SISTER,         // Hermana
   SPOUSE,         // Cónyuge
   UNCLE           // Tío
}
